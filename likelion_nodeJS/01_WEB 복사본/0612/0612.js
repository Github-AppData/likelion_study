// 배열 생성 방법 1: 대괄호([]) 사용
let fruits = ["Apple", "Banana", "Cherry"];

// 배열 생성 방법 2: Array 객체 사용
let vegetables = new Array("Carrot", "Broccoli", "Spinach");

// 배열 요소 접근
console.log(fruits[0]); // Apple
console.log(vegetables[1]); // Broccoli

// 배열 요소 수정
fruits[1] = "Blueberry";
console.log(fruits[1]); // Blueberry


